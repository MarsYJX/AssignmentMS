package com.model;

public class StudentInfo {

    private long sno;
    private String sname;

    public long getSno(){
        return sno;
    }

    public void setSno(long sno){
        this.sno = sno;
    }

    public String getSname(){
        return sname;
    }

    public void setSname(String sname){
        this.sname = sname;
    }
}
